class Solution {
    public boolean isAnagram(String s, String t) {
        int[] count = new int[26];
        for (char c : s.toCharArray()) {
            count[c - 'a']++;
        }
        for (char c : t.toCharArray()) {
            if (count[c - 'a'] < 1) {
                return false;
            }
            count[c - 'a']--;
        }
        return Arrays.stream(count).sum() == 0;
    }
}
